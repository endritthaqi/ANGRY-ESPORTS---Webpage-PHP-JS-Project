        <h1>HISTORY</h1>
        <div class="year">
          <h2>2019</h2>
          <p>
            In October 2019, when esports was rapidly gaining popularity, and
            the number of championships with big prizes kept steadily
            increasing, 4 Albanian friends "RABBIT,TIBU,SEVENTY and GJONI"
            announced the idea of ​​creating an esports team of PUBG Mobile
            representing Kosovo. Arbalet set out to create a professional team
            for which he will become the main sponsor: he will provide players
            with a training ground and will take on the financial side of the
            issue, starting with salaries and ending with the payment for
            flights.The first to respond to the proposal was starix - he was
            given the task of creating the star squad. On October 17, 2019,
            organization called ANGRY E-SPORTS (fromLatin -borntowin) was built
            around the PUBG team. It is noteworthy that initially the
            abbreviation ANGRY was borrowed from the streaming chat and its
            final form was acquired after the competition for the team name,
            conducted among the fans. The first ANGRY lineup included TIBU,
            RABBIT, SEVENTY, GJONI and FATLIND, with Rabbit becoming also the
            manager. All the players were distinguished by high individual skill
            and had a lot of experience in tournaments.
          </p>
        </div>
        <div class="year">
          <h2>2020</h2>
          <p>
            This year was very important first of all for the PUBG Mobile
            roster. The team continued to show high-class games, and won several
            major tournaments, as well as taking second places. But the
            transition to a new discipline was inevitable: by the end of 2019,
            the entire roster started to compete in PUBG Mobile. In 2011, FIFA,
            StarCraft II and League of Legends teams were created. The new
            rosters did not meet the expectations and were subsequently
            disbanded. The exception was RABBIT, who is considered one of the
            strongest PUBGMobiles players in the world at the moment and pleases
            the fans with victories. He has earned more than 20 medals in
            competitions of different rank. The main esports event of 2020 was
            The International, a STREAMERS MENA tournament with a record prize
            pool of $ 100,000.
          </p>
        </div>
        <div class="year">
          <h2>2021</h2>
          <p>
            2021 was very successful for ANGRY E-SPORTS: It was a difficult year
            for both ANGRY and esports in general. There were only a few LAN
            events and most competition moved online due to the COVID-19
            pandemic. 2021 began with a replacement in ANGRY PUBGMobile Team. A
            young gun PAKEZ joined the team and replaced the fragger. The
            renewed roster won a spot in the SUBZERO LEAGUE and third place at
            that tournament before going to REVIVAL Winter League. And there,
            the team had no equal: it’s been 2 years since the incredible
            victory at MENA TOURNAMENT, the Born to Win once again lifted the
            coveted trophy and took home $5,000 in prize money. The ANGRY
            Academy continued to grow and nurture new players. At the end of the
            year, Akili the graduate of the Academy started occasionally playing
            for the main roster and consequently became a full-time member of
            the team. The turning point was in September when the full team of
            SORRY joined the forces of ANGRY E-SPORTS. Things gradually took
            off, and in December the team took first place at many tournaments.
            In 2021, the club reopened the VALORANT division, inviting young
            players Orges and Noride, while also making a move into mobile
            esports: PUBG Mobile division was launched in the fall. Within a few
            months, the two Valorant players made more than $ 1,000, and the
            PUBG Mobile team won the Streamers League.
          </p>
        </div>
        <div class="year">
          <h2>2022</h2>
          <p>
            Despite the challenging period of the previous year, the ANGRY
            esports club went into 2022 with big plans and clear goals. The Born
            to Win focused on both consolidating achievements and reaching new
            heights, ANGRY began to appear not only on PUBG Mobile but also on
            VALORANT, conquering these disciplines is one of ANGRY’s missions.
            While existing teams boasted outstanding results: e.g. PUBG Mobile
            delivered great performances on the international stage and
            regularly made it to the top of the championships. ANGRY:PUBG Mobile
            achieved the greatest heights in 2022. The Born to Win triumphed in
            a number of tournaments, and victory at the Revival Winter League,
            Ramadan Pro League, Angry Masters League were the cherry on the
            cake. In addition, five ANGRY players (RABBIT, TIBU, PAKEZ, AKILI,
            BESARI) were included in the final Top10 rankings by HLTV.org, while
            RABBIT topped the rankings. Also in 2022, a high-tech office with
            gaming penthouses was built in Prishtina, and the club’s staff grew
            4 times.
          </p>
